from pylab import *
rc("font", size=16, family="serif", serif="Computer Sans")
rc("text", usetex=True)

prior1 = loadtxt('../Code/TTest/prior1.txt')
prior2 = loadtxt('../Code/TTest/prior2.txt')
prior3 = loadtxt('../Code/TTest/prior3.txt')
chains1 = loadtxt('../Code/TTest/chains1.txt')
chains2 = loadtxt('../Code/TTest/chains2.txt')
chains3 = loadtxt('../Code/TTest/chains3.txt')

figure(figsize=(12, 10))

subplot(2,3,1)
plot(prior1[:,0], prior1[:,1], 'b.', markersize=1)
axis('scaled')
xlabel('mu1')
ylabel('mu2')
axis([-1000, 1000, -1000, 1000])
subplot(2,3,2)
plot(prior2[:,0], prior2[:,1], 'b.', markersize=1)
axis('scaled')
xlabel('mu1')
axis([-1000, 1000, -1000, 1000])
title('Priors')
subplot(2,3,3)
plot(prior3[:,0], prior3[:,1], 'b.', markersize=1)
axis('scaled')
xlabel('mu1')
axis([-1000, 1000, -1000, 1000])

subplot(2,3,4)
plot(chains1[:,0], chains1[:,1], 'b.', markersize=1)
axis('scaled')
xlabel('mu1')
ylabel('mu2')
axis([20, 70, 20, 70])
subplot(2,3,5)
plot(chains2[:,0], chains2[:,1], 'b.', markersize=1)
axis('scaled')
xlabel('mu1')
axis([20, 70, 20, 70])
title('Posteriors')
subplot(2,3,6)
plot(chains3[:,0], chains3[:,1], 'b.', markersize=1)
axis('scaled')
xlabel('mu1')
axis([20, 70, 20, 70])
savefig('ttest.pdf', bbox_inches='tight')
show()

